public void sales()
    {
        int lastid = 0;
        try {
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");
            LocalDateTime now = LocalDateTime.now();
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/university_shop", "root", "");
            
            String total = txttotal.getText();
            String srcode = srOfUser.getText();
            
            String query = "insert into sales(srcode, Balance, Date)values(?,?,?)";
            pst = con.prepareStatement(query, Statement.RETURN_GENERATED_KEYS);
            pst.setString(1, srcode);
            pst.setString(2, total);
            pst.setString(3, dtf.format(now));
            pst.executeUpdate();
            ResultSet rs = pst.getGeneratedKeys();
                       
            if (rs.next())
            {
                lastid = rs.getInt(1);
            }
            
            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            
            String query1 = "insert into admin_sales(srcode, Name, Quantity, Date, Status)values(?,?,?,?,?)";
            
            String usersrcode = srOfUser.getText();
            
            for (int i = 0; i < model.getRowCount(); i++) {
                String prodname = (String) model.getValueAt(i, 0);
                int qty = (int) model.getValueAt(i, 2);
                pst1 = con.prepareStatement(query1, Statement.RETURN_GENERATED_KEYS);
                pst1.setString(1, usersrcode);
                pst1.setString(2, prodname);
                pst1.setInt(3, qty);
                pst1.setString(4, dtf.format(now));
                pst1.setString(5, "false");
                pst1.executeUpdate();
            }
            
        JOptionPane.showMessageDialog(this, "Order has been placed. Please print the invoice as proof of order.");
            
        } catch (SQLException ex) {
            Logger.getLogger(uniform.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(uniform.class.getName()).log(Level.SEVERE, null, ex);
        }   
    }
    
