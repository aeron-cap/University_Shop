public void receipt(){
        String total = txttotal.getText();
        
        DefaultTableModel orders = new DefaultTableModel();
        orders = (DefaultTableModel) jTable2.getModel();
        
        txtreceipt.setText(txtreceipt.getText() + "**************************************************************\n");
        txtreceipt.setText(txtreceipt.getText() + "                            Batangas State University                  \n");
        txtreceipt.setText(txtreceipt.getText() + "                         Rizal Ave, Extension, Batangas               \n");
        txtreceipt.setText(txtreceipt.getText() + "                                 University Shop                      \n");
        txtreceipt.setText(txtreceipt.getText() + "**************************************************************\n");
        
        //HEADING
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");
        LocalDateTime now = LocalDateTime.now();
        txtreceipt.setText(txtreceipt.getText() + "Date and Time of order:   " + dtf.format(now) + "\n");
        txtreceipt.setText(txtreceipt.getText() + "Student Number: " + srOfUser.getText() + "\n");
       
        txtreceipt.setText(txtreceipt.getText() + "- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -\n");
        txtreceipt.setText(txtreceipt.getText() + "Item   " + "\t" + "Quantity" + "\t" + "Price" + "\n");
        
        for(int i = 0; i < orders.getRowCount(); i++)
        {
            String prodname = (String)orders.getValueAt(i, 0);
            int qty = (int)orders.getValueAt(i, 2);
            int price = (int)orders.getValueAt(i, 3);
            
        txtreceipt.setText(txtreceipt.getText() + prodname + "\t" + qty + "\t" + price + "\n");
        
        }
        txtreceipt.setText(txtreceipt.getText() + "- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -\n");
        txtreceipt.setText(txtreceipt.getText() + "\n");
        
        txtreceipt.setText(txtreceipt.getText()+ "\t" + "\t" + "Total:" + total + "\n");
        txtreceipt.setText(txtreceipt.getText() + "\n");
        txtreceipt.setText(txtreceipt.getText() + "**************************************************************\n");
        txtreceipt.setText(txtreceipt.getText() + "                         This serves as your proof of order.           \n");
        txtreceipt.setText(txtreceipt.getText() + "                                   Thank you.                          \n");
        txtreceipt.setText(txtreceipt.getText() + "**************************************************************\n");
    }
